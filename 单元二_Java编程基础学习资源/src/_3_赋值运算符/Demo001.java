package _3_赋值运算符;

public class Demo001 {
    public static void main(String[] args) {
        //基本运算赋
        int a = 10;

        //扩展运算符
        int b = 10;
        b += 10;
        int c = 10;
        c -= 10;
        int d = 10;
        d *= 10;
        int e = 10;
        e /= 10;
        int f = 10;
        f %= 10;

        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println();
    }
}
