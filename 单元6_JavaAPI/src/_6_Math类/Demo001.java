package _6_Math类;

public class Demo001 {
    public static void main(String[] args) {
        //圆周率的静态常量
        System.out.println(Math.PI);

        //Math.abs(int num)
        System.out.println(Math.abs(-5 ));

        //Math.sin(double num)
        System.out.println(Math.sin(2.2));

        //Math.cos(double num)
        System.out.println(Math.cos(2.2));

        //Math.tan(double num)
        System.out.println(Math.tan(2.2));

        //Math.sqrt(int num)
        System.out.println(Math.sqrt(5));

        //Math.cbrt(int num)
        System.out.println(Math.cbrt(2.2));

        //Math.pow(int num1,int num2)
        System.out.println(Math.pow(2,3));

        //Math.ceil(double num)
        System.out.println(Math.ceil(2.2));

        //Math.floor(double num)
        System.out.println(Math.floor(2.2));

        //Math.round(double num)
        System.out.println(Math.round(2.1));

        //Math.max(double num1,double num2)
        System.out.println(Math.max(3,6));

        //Math.min(double num1,double num2)
        System.out.println(Math.min(3,6));

        //Math.random()
        System.out.println(Math.random());
    }
}
